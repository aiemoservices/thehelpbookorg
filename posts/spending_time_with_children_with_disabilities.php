            <p>As the founder of Helpbook Foundation, I had the privilege of spending time with children aged 5 to 10 years who have various disabilities. These children face challenges in communication, with some unable to see, hear, or speak. Despite these barriers, we engaged in bus games and activities, focusing on creating moments of joy and connection.</p>
            <p>It was heartwarming to witness their smiles and laughter as we interacted. The games were designed to be inclusive, ensuring that every child could participate in their own way. The experience deepened my understanding of the importance of empathy, patience, and creativity when working with children with disabilities. It was a reminder that, beyond words, every child has the capacity to experience happiness and to connect. My time with these children reinforced the core values of Helpbook Foundation—creating spaces where every child feels seen, heard, and valued.</p>
